        <h3>Hlavní informace</h3>
  		  Remote Control je program na vzdálené ovládání počítače. Primárně je určený např. pro odhalování nevěry, ke kontrole aktivity dětí na internetu a studijním účelům.
        <p>&nbsp;</p>
        
       <p>Komunikace probíhá přes web a soubory jsou přenášené přes FTP, pro použití programu je potřeba mít vlastní web. Lze použít některý z free hostingů, např. endoru.</p>
